/*
Calculation for number of keys needed by implicit subarrays: Extension to polyglot key system
Credit to http://hgm.nubati.net/book_format.html and the Polyglot book creators for the standard.

RandomPiece     (offset:   0, length: previously 768, now 896)
    - num squares * num colors * number of pieces = 64 * 2 * 7 = 896
RandomCastle    (offset: 768, length:   4) 
    - castle keys -> 4
        white can castle short     0
        white can castle long      1
        black can castle short     2
        black can castle long      3
RandomEnPassant (offset: 772, length:   8)
    - 0 -> a, 7 -> h
    - Stores ability for current player to en pessant a pawn in a certain file
RandomTurn      (offset: 780, length:   1)
    - If white is to move then "turn" is the sole entry of RandomTurn. If not then "turn" is zero.
*/

// Not necessary for function of engine. Preserved for posterity.
pub fn generate_zobrist_keys() -> String {
    // We use a pseudorandom number generator per recommendation of the chess programming wiki.
    let mut gen = rand_chacha::ChaCha8Rng::seed_from_u64(123094);
    let zobrist_arr : [u64; 909] = (0..909).map(|_| gen.next_u64())
        .collect::<Vec<u64>>()
        .try_into()
        .unwrap();
        
    let mut ret : String = String::from("");
    for idx in 0..909 {
        let num_str : String = format!("{:x}", zobrist_arr[idx]);
        let necessary_buffer = 16 - num_str.chars().count();
        let buffer : String = (0..necessary_buffer).map(|_| "0").collect::<String>();
        ret = format!("{}0x{}{},\n", ret, buffer, num_str.to_uppercase());
    }
    
    ret
}

